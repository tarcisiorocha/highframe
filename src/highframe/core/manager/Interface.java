package highframe.core.manager;

public class Interface {
	private String idInterface;
	private String nameInterface;
	private String signature;
	private String interfaceType;
	
	public String getIdInterface() {
		return idInterface;
	}
	public void setIdInterface(String idInterface) {
		this.idInterface = idInterface;
	}
	public String getNameInterface() {
		return nameInterface;
	}
	public void setNameInterface(String nameInterface) {
		this.nameInterface = nameInterface;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public String getInterfaceType() {
		return interfaceType;
	}
	public void setInterfaceType(String interfaceType) {
		this.interfaceType = interfaceType;
	}
}
